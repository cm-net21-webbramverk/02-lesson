// i är rader (y), j är kolumner (x)
let text = '';
for( let i=0; i<6; i++ ) {
    for( let j=0; j<8; j++ ) {
        if( (i*8 + j) % 3 === 0 )
            text += '.';
		else if( (i*8 + j) % 3 === 1 )
			text += '#'
        else
            text += 'O';
    }
    text += '\n';
}
console.log(text);


